package enumeradores;

/**
 * Enumeración que representa las acciones que se pueden realizar dentro del catálogo.
 * 
 * @author Juventino López García
 * @author Diego Valenzuela Parra
 */
public enum AccionCatalogoEnumerador {
    NUEVO,      // Acción para crear un nuevo elemento en el catálogo
    EDITAR,     // Acción para editar un elemento existente en el catálogo
    ELIMINAR,   // Acción para eliminar un elemento del catálogo
    BUSCAR,     // Acción para buscar elementos en el catálogo
    DEPOSITO,   // Acción para realizar un depósito
    RETIRO      // Acción para realizar un retiro
}
